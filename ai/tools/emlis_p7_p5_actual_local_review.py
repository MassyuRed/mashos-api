def record_pass_a_selection(
    *,
    local_root: str | Path | None,
    session_id: Any,
    ordinal: int,
    reviewer_ref: Any,
    axis_scores: Mapping[str, Any],
    verdict: Any,
    sanitized_reason_ids: Sequence[Any] | None,
    blocker_ids: Sequence[Any] | None,
    human_entry_attested: bool,
    local_note: str = "",
    reviewed_at: str | None = None,
) -> dict[str, Any]:
    """Record one explicit human Pass-A selection and freeze that row."""

    layout = _layout(local_root, session_id)
    state = _load_state(layout)
    _enforce_review_retention(state)
    if state.get("state_ref") != STATE_PASS_A_OPEN:
        raise ReviewOperationError("Pass Aはopen state以外で変更できません")
    if human_entry_attested is not True:
        raise ReviewOperationError("Pass A rowはhuman entry attestationが必要です")
    human = _human_ref(reviewer_ref, field="reviewer_ref")
    if human != state.get("pass_a_human_reviewer_ref"):
        raise ReviewOperationError("Pass A reviewer refがprepared sessionと一致しません")
    if not 1 <= ordinal <= EXPECTED_CASE_COUNT:
        raise ReviewOperationError("Pass A ordinalは1〜24です")
    controller = _controller(layout)
    crow = _controller_row(controller, ordinal)
    reasons = _identifier_list(
        sanitized_reason_ids,
        field="sanitized_reason_ids",
        allowed=P7_R48_SANITIZED_REASON_ID_REFS,
    )
    blockers = _identifier_list(
        blocker_ids,
        field="blocker_ids",
        allowed=P7_R48_READFEEL_BLOCKER_ID_REFS,
    )
    timestamp = reviewed_at or _iso()
    _parse_timestamp(timestamp, field="reviewed_at")
    ephemeral_note_supplied = _validate_ephemeral_note(local_note)
    review_result = {
        "axis_scores": dict(axis_scores),
        "verdict": _safe_identifier(verdict, field="verdict", max_length=60),
        "sanitized_reason_ids": reasons,
        "blocker_ids": blockers,
    }
    row = normalize_p7_r49_p5_rating_row_via_r48_bodyfree(
        review_result=review_result,
        case_manifest_row=_case_row_for_existing_normalizers(crow),
        review_session_id=layout.session_id,
        reviewer_ref=human,
        reviewed_at=timestamp,
        body_removed=False,
    )
    assert_p7_r48_p5_rating_row_bodyfree_contract(row)
    form = {
        "schema_version": PASS_A_FORM_SCHEMA_VERSION,
        "review_session_id": layout.session_id,
        "review_unit_ref": crow["review_unit_ref"],
        "blind_case_id": crow["blind_case_id"],
        "human_reviewer_ref": human,
        "human_entry_attested": True,
        "recorded_at": timestamp,
        "axis_scores": dict(row["axis_scores"]),
        "verdict": row["verdict"],
        "sanitized_reason_ids": list(row["sanitized_reason_ids"]),
        "blocker_ids": list(row["blocker_ids"]),
        "reviewer_free_text_included": False,
        "ephemeral_local_note_supplied": ephemeral_note_supplied,
        "ephemeral_local_note_persisted": False,
        "ephemeral_local_note_disposed_at_row_freeze": ephemeral_note_supplied,
        "body_free": True,
    }
    _assert_no_forbidden_bodyfree_keys(form, source="pass_a_form")
    _write_json_exclusive(layout.pass_a_forms_dir / _form_filename(ordinal), form)
    _write_json_exclusive(layout.pass_a_rows_dir / _row_filename(ordinal), row, mode=0o400)
    count = _count_rows(layout.pass_a_rows_dir)
    state = _save_state(layout, state, pass_a_completed_count=count)
    if count == EXPECTED_CASE_COUNT:
        rows = _aggregate_rows(
            layout=layout,
            rows_dir=layout.pass_a_rows_dir,
            target_file=layout.body_free_results_dir / "pass_a_rating_rows.bodyfree.json",
            row_kind_ref="R48_P5_HUMAN_RATING_ROW",
        )
        for item in rows:
            assert_p7_r48_p5_rating_row_bodyfree_contract(item)
        frozen_at = _iso()
        _freeze_tree(layout.pass_a_forms_dir)
        _freeze_tree(layout.pass_a_rows_dir)
        state = _save_state(
            layout,
            state,
            new_state=STATE_PASS_A_FROZEN,
            pass_a_frozen_at=frozen_at,
        )
    return _bodyfree_status(layout, state=state)


