def _validate_history_records(value: Any, *, ordinal: int) -> list[dict[str, str]]:
    if not isinstance(value, list):
        raise ReviewOperationError(f"case {ordinal}: bounded historyはrecord配列が必要です")
    if not 2 <= len(value) <= 3:
        raise ReviewOperationError(f"case {ordinal}: bounded historyは2〜3 recordが必要です")
    out: list[dict[str, str]] = []
    for index, raw in enumerate(value, 1):
        if not isinstance(raw, Mapping) or set(raw) != HISTORY_RECORD_REQUIRED_FIELDS:
            raise ReviewOperationError(f"case {ordinal}: history record {index}のfieldが不正です")
        relative = _safe_identifier(
            raw.get("relative_time_ref"),
            field=f"case {ordinal}.history.relative_time_ref",
            max_length=80,
        )
        surface = _body_text(
            raw.get("review_surface"),
            field=f"case {ordinal}.history.review_surface",
            max_chars=12_000,
        )
        out.append({"relative_time_ref": relative, "review_surface": surface})
    return out


def _validate_source_manifest(layout: SessionLayout) -> dict[str, Any]:
    manifest = _load_json(layout.source_manifest_file)
    if set(manifest) != SOURCE_MANIFEST_REQUIRED_FIELDS:
        raise ReviewOperationError("actual source manifestのtop-level fieldがexact schemaと一致しません")
    if manifest.get("schema_version") != SOURCE_MANIFEST_SCHEMA_VERSION:
        raise ReviewOperationError("actual source manifest schema_versionが一致しません")
    if manifest.get("manifest_status_ref") != SOURCE_MANIFEST_READY_STATUS:
        raise ReviewOperationError("source manifestはACTUAL_CURRENT_24_SOURCE_BODY_MATERIALへ明示更新してください")
    if manifest.get("review_session_id") != layout.session_id:
        raise ReviewOperationError("source manifest review_session_idがsessionと一致しません")
    if manifest.get("body_full_local_only") is not True or manifest.get("must_not_export") is not True:
        raise ReviewOperationError("source manifestはlocal-only / must-not-export固定です")
    if manifest.get("currentness_status_ref") != CURRENTNESS_READY_STATUS:
        raise ReviewOperationError("source manifest currentnessはCURRENT_EXPLICITが必要です")
    snapshot_ref = _assert_actual_ref(manifest.get("snapshot_ref"), field="snapshot_ref")
    runtime_ref = _assert_actual_ref(manifest.get("runtime_revision_ref"), field="runtime_revision_ref")
    source_material_ref = _assert_actual_ref(manifest.get("source_material_ref"), field="source_material_ref")
    supplied_instruction_ref = _assert_actual_ref(
        manifest.get("supplied_user_execution_instruction_ref"),
        field="supplied_user_execution_instruction_ref",
    )
    created_at = _parse_timestamp(
        manifest.get("created_at"),
        field="source_manifest.created_at",
    )
    if created_at > _now():
        raise ReviewOperationError("source manifest created_atを未来時刻にできません")

    rows = manifest.get("case_rows")
    if not isinstance(rows, list) or len(rows) != EXPECTED_CASE_COUNT:
        raise ReviewOperationError("actual source manifestは24件exactである必要があります")

    expected_by_ordinal: dict[int, tuple[str, str]] = {}
    ordinal = 1
    for family, count, role in _expected_distribution():
        for _ in range(count):
            expected_by_ordinal[ordinal] = (family, role)
            ordinal += 1

    normalized_rows: list[dict[str, Any]] = []
    case_refs: set[str] = set()
    source_case_refs: set[str] = set()
    family_counts: Counter[str] = Counter()
    for expected_ordinal, raw in enumerate(rows, 1):
        if not isinstance(raw, Mapping) or set(raw) != SOURCE_CASE_REQUIRED_FIELDS:
            raise ReviewOperationError(f"case {expected_ordinal}: source row fieldがexact schemaと一致しません")
        if raw.get("ordinal") != expected_ordinal:
            raise ReviewOperationError("source manifest ordinalは1〜24の固定順である必要があります")
        expected_family, expected_role = expected_by_ordinal[expected_ordinal]
        family = _safe_identifier(raw.get("family"), field=f"case {expected_ordinal}.family")
        role = _safe_identifier(raw.get("case_role"), field=f"case {expected_ordinal}.case_role")
        if (family, role) != (expected_family, expected_role):
            raise ReviewOperationError(f"case {expected_ordinal}: R48 family / role配分が一致しません")
        tier = _safe_identifier(
            raw.get("subscription_tier_ref"),
            field=f"case {expected_ordinal}.subscription_tier_ref",
        )
        if tier not in SUBSCRIPTION_TIER_REFS:
            raise ReviewOperationError(f"case {expected_ordinal}: subscription tierが不正です")
        if family == "free_tier_history_present_not_allowed":
            if tier != "free":
                raise ReviewOperationError(f"case {expected_ordinal}: Free boundary familyはtier=freeが必要です")
        elif tier == "free":
            raise ReviewOperationError(f"case {expected_ordinal}: Free tierは専用boundary family以外へ混ぜられません")
        expected_audit = _safe_identifier(
            raw.get("expected_boundary_audit_ref"),
            field=f"case {expected_ordinal}.expected_boundary_audit_ref",
        )
        if expected_audit != _expected_audit_ref(family):
            raise ReviewOperationError(f"case {expected_ordinal}: expected boundary audit refがfamilyと一致しません")
        case_ref = _assert_actual_ref(raw.get("case_ref_id"), field=f"case {expected_ordinal}.case_ref_id")
        source_case_ref = _assert_actual_ref(
            raw.get("source_case_ref"),
            field=f"case {expected_ordinal}.source_case_ref",
        )
        if case_ref in case_refs or source_case_ref in source_case_refs:
            raise ReviewOperationError("source manifest case refsは24件uniqueである必要があります")
        case_refs.add(case_ref)
        source_case_refs.add(source_case_ref)
        source_snapshot = _assert_actual_ref(
            raw.get("source_snapshot_ref"),
            field=f"case {expected_ordinal}.source_snapshot_ref",
        )
        source_runtime = _assert_actual_ref(
            raw.get("source_runtime_ref"),
            field=f"case {expected_ordinal}.source_runtime_ref",
        )
        if source_snapshot != snapshot_ref or source_runtime != runtime_ref:
            raise ReviewOperationError(
                f"case {expected_ordinal}: source snapshot/runtime refが"
                "top-level current basisと一致しません"
            )
        if raw.get("currentness_status_ref") != CURRENTNESS_READY_STATUS:
            raise ReviewOperationError(f"case {expected_ordinal}: currentnessはCURRENT_EXPLICITが必要です")
        body_source_kind = _safe_identifier(
            raw.get("body_source_kind_ref"),
            field=f"case {expected_ordinal}.body_source_kind_ref",
        )
        if body_source_kind not in BODY_SOURCE_KIND_REFS:
            raise ReviewOperationError(f"case {expected_ordinal}: actual body source kindが不正です")
        current_input = _body_text(
            raw.get("current_input_review_surface"),
            field=f"case {expected_ordinal}.current_input_review_surface",
            max_chars=20_000,
        )
        returned = _body_text(
            raw.get("returned_emlis_surface"),
            field=f"case {expected_ordinal}.returned_emlis_surface",
            max_chars=40_000,
        )
        history = _validate_history_records(raw.get("bounded_owned_history_review_surface"), ordinal=expected_ordinal)
        family_counts[family] += 1
        normalized_rows.append(
            {
                "ordinal": expected_ordinal,
                "case_ref_id": case_ref,
                "source_case_ref": source_case_ref,
                "family": family,
                "case_role": role,
                "subscription_tier_ref": tier,
                "expected_boundary_audit_ref": expected_audit,
                "source_snapshot_ref": source_snapshot,
                "source_runtime_ref": source_runtime,
                "currentness_status_ref": CURRENTNESS_READY_STATUS,
                "body_source_kind_ref": body_source_kind,
                "current_input_review_surface": current_input,
                "returned_emlis_surface": returned,
                "bounded_owned_history_review_surface": history,
            }
        )

    expected_counts = {family: count for family, count, _role in _expected_distribution()}
    if dict(family_counts) != expected_counts:
        raise ReviewOperationError("source manifest family distributionがR48 exact 24配分と一致しません")
    return {
        **{
            key: manifest[key]
            for key in SOURCE_MANIFEST_REQUIRED_FIELDS
            if key != "case_rows"
        },
        "created_at": _iso(created_at),
        "snapshot_ref": snapshot_ref,
        "runtime_revision_ref": runtime_ref,
        "source_material_ref": source_material_ref,
        "supplied_user_execution_instruction_ref": supplied_instruction_ref,
        "case_rows": normalized_rows,
    }


def _bounded_history_string(records: Sequence[Mapping[str, Any]]) -> str:
    parts: list[str] = []
    for record in records:
        parts.append(f"[{record['relative_time_ref']}]\n{record['review_surface']}")
    return "\n\n---\n\n".join(parts)


