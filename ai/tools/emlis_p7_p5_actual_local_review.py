"""Local-only P5 actual 24-case human readfeel review controller.

This command-line tool is the minimal operation layer that was intentionally
absent from P7-R47 through P7-R51.  It does not change Emlis runtime, API, DB,
RN, Gate, subscription rules, or public response contracts.

The controller:

* accepts only an explicit current 24-case source/body manifest kept under the
  external local review root;
* requires the existing R50 and R51 scoped allow environment values before it
  materializes any body-full reviewer packet;
* uses the existing R48 packet/rating contract and R49 question-observation
  normalizer rather than replacing them;
* enforces Pass A -> Pass B -> Pass C as an irreversible state machine;
* keeps Pass A and Pass B blind, and never reopens body-full material in Pass C;
* freezes independent pseudonymous human refs for Pass A, Pass B, and Pass C;
* stores body-full source material, packets, and reviewer forms only below the
  external local review root;
* validates optional reviewer free text but discards it immediately at row
  freeze instead of persisting it;
* never derives identifiers or hashes from user text;
* physically purges local-only material and emits only body-free disposal and
  handoff material.

Human ratings and semantic selections are never generated by this module.  The
interactive commands display one case at a time and require explicit reviewer
input.  Programmatic recording helpers require an explicit human-entry
attestation and still validate all existing body-free contracts.
"""

