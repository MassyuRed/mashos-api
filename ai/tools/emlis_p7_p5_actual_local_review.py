def _start_pass(
    layout: SessionLayout,
    state: Mapping[str, Any],
    *,
    current: set[str],
    open_state: str,
) -> dict[str, Any]:
    if state.get("state_ref") not in current:
        raise ReviewOperationError("現在stateではこのPassを開始・再開できません")
    if state.get("state_ref") != open_state:
        return _save_state(layout, state, new_state=open_state)
    return dict(state)


