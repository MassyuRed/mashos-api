def _validate_state(state: Mapping[str, Any], *, session_id: str) -> dict[str, Any]:
    data = dict(state)
    _assert_no_forbidden_bodyfree_keys(data, source="session_state")
    if data.get("schema_version") != STATE_SCHEMA_VERSION or data.get("review_session_id") != session_id:
        raise ReviewOperationError("session state schema / identityが一致しません")
    if data.get("body_free") is not True:
        raise ReviewOperationError("session stateはbody-freeである必要があります")
    if data.get("body_full_packet_exported") is not False or data.get("body_derived_hash_stored") is not False:
        raise ReviewOperationError("session stateにbody export / hash violationがあります")
    if data.get("local_absolute_path_included") is not False:
        raise ReviewOperationError("session stateにabsolute path markerがあります")
    allowed_states = REVIEW_STATES_WITH_BODY | TERMINAL_STATES | {
        STATE_INITIALIZED,
        STATE_PURGE_IN_PROGRESS,
    }
    if data.get("state_ref") not in allowed_states:
        raise ReviewOperationError("session state_refが許可stateにありません")
    for key in (
        "body_full_packet_count",
        "pass_a_completed_count",
        "pass_b_completed_count",
        "pass_c_completed_count",
    ):
        value = data.get(key)
        if not isinstance(value, int) or isinstance(value, bool) or not 0 <= value <= EXPECTED_CASE_COUNT:
            raise ReviewOperationError(f"session state {key}は0〜24の整数が必要です")
    for key in (
        "pass_a_human_reviewer_ref",
        "pass_b_human_reviewer_ref",
        "pass_c_plan_overlay_resolver_ref",
    ):
        value = data.get(key)
        if value is not None:
            _human_ref(value, field=key)
    if int(data.get("pass_a_completed_count") or 0) > 0 and data.get("pass_a_human_reviewer_ref") is None:
        raise ReviewOperationError("Pass A rowがあるstateにはPass A human refが必要です")
    if int(data.get("pass_b_completed_count") or 0) > 0 and data.get("pass_b_human_reviewer_ref") is None:
        raise ReviewOperationError("Pass B rowがあるstateにはPass B human refが必要です")
    if int(data.get("pass_c_completed_count") or 0) > 0 and data.get("pass_c_plan_overlay_resolver_ref") is None:
        raise ReviewOperationError("Pass C rowがあるstateにはPass C resolver refが必要です")
    if data.get("reviewer_notes_persisted") is not False or data.get("reviewer_notes_immediate_disposal") is not True:
        raise ReviewOperationError("reviewer free textは永続化せずrow freeze時に破棄する必要があります")
    source_created_at = data.get("source_body_material_created_at")
    body_full_created_at = data.get("body_full_created_at")
    body_full_deadline = data.get("body_full_retention_deadline")
    parsed_source_created_at = (
        _parse_timestamp(source_created_at, field="source_body_material_created_at")
        if source_created_at is not None
        else None
    )
    parsed_body_full_created_at = (
        _parse_timestamp(body_full_created_at, field="body_full_created_at")
        if body_full_created_at is not None
        else None
    )
    parsed_body_full_deadline = (
        _parse_timestamp(body_full_deadline, field="body_full_retention_deadline")
        if body_full_deadline is not None
        else None
    )
    if data.get("body_full_material_present") is True:
        if None in {
            parsed_source_created_at,
            parsed_body_full_created_at,
            parsed_body_full_deadline,
        }:
            raise ReviewOperationError("body-full material stateには生成時刻とretention deadlineが必要です")
        if not (
            parsed_source_created_at
            <= parsed_body_full_created_at
            <= parsed_body_full_deadline
        ):
            raise ReviewOperationError("body-full material timestamp順が成立しません")
        maximum_deadline = parsed_source_created_at + timedelta(
            hours=P7_R47_BODY_FULL_PACKET_RETENTION_HOURS
        )
        if parsed_body_full_deadline > maximum_deadline:
            raise ReviewOperationError("body-full retentionが最初のlocal body material生成から72時間を超えています")
    purge_mode = data.get("purge_mode_ref")
    purge_counts = data.get("purge_target_entry_counts")
    if purge_mode is None:
        if purge_counts not in ({}, None) or data.get("purge_started_at") is not None:
            raise ReviewOperationError("purge未開始stateにpurge journalを残せません")
    else:
        if purge_mode not in {"COMPLETE_REVIEW", "ABORTED_REVIEW"}:
            raise ReviewOperationError("purge mode refが不正です")
        if not isinstance(purge_counts, Mapping) or set(purge_counts) != set(
            P7_R51_PURGE_PLAN_REQUIRED_DELETE_TARGET_REFS
        ):
            raise ReviewOperationError("purge journal target countがexact 3対象と一致しません")
        for value in purge_counts.values():
            if not isinstance(value, int) or isinstance(value, bool) or value < 1:
                raise ReviewOperationError("purge journal entry countは1以上の整数が必要です")
        _parse_timestamp(data.get("purge_started_at"), field="purge_started_at")
        if data.get("state_ref") not in {
            STATE_PURGE_IN_PROGRESS,
            STATE_DISPOSAL_VERIFIED,
            STATE_ABORTED_PURGED,
        }:
            raise ReviewOperationError("purge journalをreview open stateへ混在できません")
    for key in (
        "p5_decision_made_here",
        "p5_confirmed_candidate",
        "p6_start_allowed",
        "p8_start_allowed",
        "release_allowed",
    ):
        if data.get(key) is not False:
            raise ReviewOperationError(f"local controllerは{key}=Falseを維持する必要があります")
    return data


