def _now() -> datetime:
    return datetime.now(timezone.utc).replace(microsecond=0)


def _iso(value: datetime | None = None) -> str:
    return (value or _now()).isoformat()


def _parse_timestamp(value: Any, *, field: str) -> datetime:
    if not isinstance(value, str) or not value.strip():
        raise ReviewOperationError(f"{field} はtimezone付きISO-8601時刻が必要です")
    text = value.strip().replace("Z", "+00:00")
    try:
        parsed = datetime.fromisoformat(text)
    except ValueError as exc:
        raise ReviewOperationError(f"{field} はtimezone付きISO-8601時刻が必要です") from exc
    if parsed.tzinfo is None or parsed.utcoffset() is None:
        raise ReviewOperationError(f"{field} はtimezoneを省略できません")
    return parsed


def _safe_identifier(value: Any, *, field: str, max_length: int = 160) -> str:
    if not isinstance(value, str):
        raise ReviewOperationError(f"{field} は文字列refが必要です")
    text = value.strip()
    if not text or len(text) > max_length or not _IDENTIFIER_RE.fullmatch(text):
        raise ReviewOperationError(f"{field} は安全なbody-free ref形式が必要です")
    return text


def _human_ref(value: Any, *, field: str) -> str:
    if not isinstance(value, str) or not HUMAN_REF_RE.fullmatch(value.strip()):
        raise ReviewOperationError(f"{field} は human- で始まるpseudonymous human refが必要です")
    text = value.strip()
    lowered_tokens = set(re.split(r"[_.:-]+", text.lower()))
    if lowered_tokens & {"ai", "bot", "model", "gpt", "llm", "automatic", "system", "karen", "華恋"}:
        raise ReviewOperationError(f"{field} にmachine / AI refは使用できません")
    return text


def _identifier_list(
    value: Any,
    *,
    field: str,
    allowed: Sequence[str] | set[str] | frozenset[str] | None = None,
    allow_empty: bool = True,
) -> list[str]:
    if value is None:
        values: list[Any] = []
    elif isinstance(value, Sequence) and not isinstance(value, (str, bytes, bytearray)):
        values = list(value)
    else:
        raise ReviewOperationError(f"{field} はref配列が必要です")
    out: list[str] = []
    for item in values:
        ref = _safe_identifier(item, field=field)
        if ref in out:
            raise ReviewOperationError(f"{field} にduplicate refがあります")
        out.append(ref)
    if not allow_empty and not out:
        raise ReviewOperationError(f"{field} は1件以上必要です")
    if allowed is not None and any(ref not in set(allowed) for ref in out):
        raise ReviewOperationError(f"{field} に許可外refがあります")
    return out


def _is_placeholder_ref(value: str) -> bool:
    lowered = value.strip().lower()
    if not lowered:
        return True
    if lowered.startswith("p7r48-p5-case-") or lowered.startswith("p7r48-blind-"):
        return True
    return any(fragment in lowered for fragment in PLACEHOLDER_FRAGMENTS)


def _assert_actual_ref(value: Any, *, field: str) -> str:
    ref = _safe_identifier(value, field=field)
    if _is_placeholder_ref(ref):
        raise ReviewOperationError(f"{field} にdefault / synthetic / unresolved refは使用できません")
    return ref


def _assert_no_forbidden_bodyfree_keys(value: Any, *, source: str) -> None:
    if isinstance(value, Mapping):
        for key, child in value.items():
            if str(key) in BODYFREE_FORBIDDEN_FIELD_REFS:
                raise ReviewOperationError(f"{source} にbody / text / path / hash禁止fieldがあります")
            _assert_no_forbidden_bodyfree_keys(child, source=source)
    elif isinstance(value, Sequence) and not isinstance(value, (str, bytes, bytearray)):
        for child in value:
            _assert_no_forbidden_bodyfree_keys(child, source=source)


def _body_text(value: Any, *, field: str, max_chars: int) -> str:
    if not isinstance(value, str) or not value.strip():
        raise ReviewOperationError(f"{field} にactual review surfaceが必要です")
    normalized = value.replace("\r\n", "\n").replace("\r", "\n")
    unsafe_controls = any(
        (ord(character) < 32 and character not in {"\n", "\t"})
        or ord(character) == 127
        for character in normalized
    )
    if unsafe_controls or len(normalized) > max_chars:
        raise ReviewOperationError(f"{field} がlocal review上限を超えるか不正です")
    return normalized


def _load_json(path: Path, *, max_bytes: int = 8_000_000) -> dict[str, Any]:
    try:
        if path.is_symlink():
            raise ReviewOperationError("symlink経由のlocal review fileは使用できません")
        size = path.stat().st_size
        if size > max_bytes:
            raise ReviewOperationError("local review JSONが許容サイズを超えています")
        with path.open("r", encoding="utf-8") as handle:
            value = json.load(handle)
    except FileNotFoundError as exc:
        raise ReviewOperationError("必要なlocal review fileがありません") from exc
    except json.JSONDecodeError as exc:
        raise ReviewOperationError("local review JSONを安全に読み取れません") from exc
    if not isinstance(value, dict):
        raise ReviewOperationError("local review JSON rootはobjectである必要があります")
    return value


def _json_bytes(value: Mapping[str, Any]) -> bytes:
    return (json.dumps(value, ensure_ascii=False, indent=2, sort_keys=True) + "\n").encode("utf-8")


def _assert_no_symlink(path: Path) -> None:
    if path.exists() and path.is_symlink():
        raise ReviewOperationError("local review pathにsymlinkは使用できません")


def _write_json_exclusive(path: Path, value: Mapping[str, Any], *, mode: int = 0o600) -> None:
    _assert_no_symlink(path)
    path.parent.mkdir(parents=True, exist_ok=True, mode=0o700)
    flags = os.O_WRONLY | os.O_CREAT | os.O_EXCL
    if hasattr(os, "O_NOFOLLOW"):
        flags |= os.O_NOFOLLOW
    try:
        fd = os.open(path, flags, mode)
    except FileExistsError as exc:
        raise ReviewOperationError("freeze済みrowは上書きできません") from exc
    try:
        with os.fdopen(fd, "wb", closefd=True) as handle:
            handle.write(_json_bytes(value))
            handle.flush()
            os.fsync(handle.fileno())
    except Exception:
        try:
            path.unlink(missing_ok=True)
        finally:
            raise
    os.chmod(path, mode)


def _write_json_atomic(path: Path, value: Mapping[str, Any], *, mode: int = 0o600) -> None:
    _assert_no_symlink(path)
    path.parent.mkdir(parents=True, exist_ok=True, mode=0o700)
    temp = path.parent / f".{path.name}.{secrets.token_hex(8)}.tmp"
    flags = os.O_WRONLY | os.O_CREAT | os.O_EXCL
    if hasattr(os, "O_NOFOLLOW"):
        flags |= os.O_NOFOLLOW
    fd = os.open(temp, flags, mode)
    try:
        with os.fdopen(fd, "wb", closefd=True) as handle:
            handle.write(_json_bytes(value))
            handle.flush()
            os.fsync(handle.fileno())
        os.replace(temp, path)
        os.chmod(path, mode)
    finally:
        temp.unlink(missing_ok=True)


def _validate_ephemeral_note(text: str) -> bool:
    """Validate optional reviewer free text, then discard it immediately.

    The operation layer deliberately does not persist reviewer free text.  The
    R47 24-hour upper bound is therefore satisfied by immediate disposal at
    row freeze, while structured reason/blocker refs remain body-free.
    """

    if not isinstance(text, str):
        raise ReviewOperationError("reviewer noteは文字列である必要があります")
    if not text.strip():
        return False
    if "\x00" in text or len(text) > 12_000:
        raise ReviewOperationError("reviewer noteがlocal-only上限を超えるか不正です")
    return True


def _freeze_tree(path: Path) -> None:
    if not path.exists():
        return
    if path.is_symlink():
        raise ReviewOperationError("freeze対象にsymlinkがあります")
    if path.is_file():
        os.chmod(path, 0o400)
        return
    for child in path.iterdir():
        _freeze_tree(child)
    os.chmod(path, 0o500)


def _thaw_tree(path: Path) -> None:
    if not path.exists():
        return
    if path.is_symlink():
        raise ReviewOperationError("purge対象にsymlinkがあります")
    if path.is_dir():
        os.chmod(path, 0o700)
        for child in path.iterdir():
            _thaw_tree(child)
    else:
        os.chmod(path, 0o600)


def _entry_count(path: Path) -> int:
    if not path.exists():
        return 0
    count = 1
    if path.is_dir():
        for child in path.iterdir():
            count += _entry_count(child)
    return count


def _path_under(child: Path, parent: Path) -> bool:
    try:
        child.resolve().relative_to(parent.resolve())
        return True
    except ValueError:
        return False


def _resolve_local_root(local_root: str | Path | None, *, create: bool) -> Path:
    raw = (
        str(local_root).strip()
        if local_root is not None
        else os.environ.get(P7_R47_LOCAL_REVIEW_ROOT_ENV_VAR, "").strip()
    )
    if not raw:
        raise ReviewOperationError(f"{P7_R47_LOCAL_REVIEW_ROOT_ENV_VAR} が設定されていません")
    candidate = Path(raw).expanduser()
    if not candidate.is_absolute():
        raise ReviewOperationError("local review rootはabsolute pathである必要があります")
    initial_policy = build_p7_r47_local_review_storage_root_policy(
        local_review_root=str(candidate),
        repo_roots=[str(_AI_ROOT.parent)],
    )
    assert_p7_r47_local_review_storage_root_policy_contract(initial_policy)
    if initial_policy.get("local_review_root_status") != "valid":
        reasons = ",".join(initial_policy.get("generation_block_reason_ids") or ["invalid_local_review_root"])
        raise ReviewOperationError(f"local review rootがR47境界を満たしません: {reasons}")
    if create:
        candidate.mkdir(parents=True, exist_ok=True, mode=0o700)
        os.chmod(candidate, 0o700)
    if not candidate.exists() or not candidate.is_dir() or candidate.is_symlink():
        raise ReviewOperationError("local review rootは実在する通常directoryである必要があります")
    resolved = candidate.resolve()
    resolved_policy = build_p7_r47_local_review_storage_root_policy(
        local_review_root=str(resolved),
        repo_roots=[str(_AI_ROOT.parent.resolve())],
    )
    assert_p7_r47_local_review_storage_root_policy_contract(resolved_policy)
    if resolved_policy.get("local_review_root_status") != "valid":
        raise ReviewOperationError("resolved local review rootがR47境界を満たしません")
    return resolved


