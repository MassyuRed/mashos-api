def purge_session(
    *,
    local_root: str | Path | None,
    session_id: Any,
    abort: bool = False,
) -> dict[str, Any]:
    """Physically remove local-only body/forms/notes and write body-free evidence."""

    layout = _layout(local_root, session_id)
    state = _load_state(layout)
    current_state = state.get("state_ref")
    if current_state in TERMINAL_STATES:
        return _bodyfree_status(layout, state=state)
    target_paths = {
        "body_full_packets.local_only": layout.body_full_dir,
        "reviewer_forms.local_only": layout.reviewer_forms_dir,
        "reviewer_notes.local_only": layout.reviewer_notes_dir,
    }

    if current_state == STATE_PURGE_IN_PROGRESS:
        purge_mode = state.get("purge_mode_ref")
        complete = purge_mode == "COMPLETE_REVIEW"
        target_entry_counts = dict(state.get("purge_target_entry_counts") or {})
        purge_started = str(state.get("purge_started_at"))
    else:
        complete = current_state == STATE_PURGE_REQUIRED
        if not complete and not abort:
            raise ReviewOperationError("complete Pass C以外のpurgeはabort=Trueが必要です")
        if complete and any(
            int(state.get(key) or 0) != EXPECTED_CASE_COUNT
            for key in (
                "pass_a_completed_count",
                "pass_b_completed_count",
                "pass_c_completed_count",
            )
        ):
            raise ReviewOperationError("24/24/24 body-free rowsが揃うまでverified purgeへ進めません")
        target_entry_counts: dict[str, int] = {}
        for target_ref in P7_R51_PURGE_PLAN_REQUIRED_DELETE_TARGET_REFS:
            path = target_paths[target_ref]
            if not path.exists() or path.is_symlink():
                raise ReviewOperationError(
                    "purge開始前にrequired local-only targetの実在を確認できません"
                )
            count = _entry_count(path)
            if count < 1:
                raise ReviewOperationError("purge target entry countを確認できません")
            target_entry_counts[target_ref] = count
        purge_started = _iso()
        state = _save_state(
            layout,
            state,
            new_state=STATE_PURGE_IN_PROGRESS,
            purge_mode_ref="COMPLETE_REVIEW" if complete else "ABORTED_REVIEW",
            purge_target_entry_counts=target_entry_counts,
            purge_started_at=purge_started,
        )

    evidence_rows: list[dict[str, Any]] = []
    total_deleted_entries = 0
    for target_ref in P7_R51_PURGE_PLAN_REQUIRED_DELETE_TARGET_REFS:
        path = target_paths[target_ref]
        if path.exists():
            _thaw_tree(path)
            shutil.rmtree(path)
        removed = not path.exists()
        if not removed:
            raise ReviewOperationError("local-only purge verificationに失敗しました")
        removed_count = int(target_entry_counts[target_ref])
        total_deleted_entries += removed_count
        evidence_rows.append(
            build_p7_r51_purge_evidence_row_bodyfree(
                review_session_id=layout.session_id,
                purge_target_ref=target_ref,
                purge_target_kind=target_ref,
                purge_required=True,
                purge_attempted=True,
                removed=True,
                removed_count=removed_count,
                verification_status_ref="REMOVED_AND_VERIFIED",
            )
        )
    purge_completed = _iso()
    evidence_collection = {
        "schema_version": PURGE_EVIDENCE_COLLECTION_SCHEMA_VERSION,
        "review_session_id": layout.session_id,
        "purge_evidence_row_count": len(evidence_rows),
        "purge_evidence_rows": evidence_rows,
        "local_absolute_path_included": False,
        "body_content_hash_included": False,
        "deleted_body_preview_included": False,
        "terminal_output_included": False,
        "body_free": True,
    }
    _assert_no_forbidden_bodyfree_keys(evidence_collection, source="r51_purge_evidence_collection")
    _write_json_atomic(
        layout.audit_dir / "r51_purge_evidence_rows.bodyfree.json",
        evidence_collection,
        mode=0o400,
    )

    expired = _retention_expired(state)
    disposal_status = "DISPOSAL_VERIFIED" if complete else "EXPIRED_PURGED" if expired else "BODY_PURGED"
    receipt = {
        "schema_version": P7_R47_BODY_FREE_DISPOSAL_RECEIPT_SCHEMA_VERSION,
        "review_session_id": layout.session_id,
        "packet_kind": P7_R48_PACKET_KIND,
        "case_count": int(state.get("body_full_packet_count") or 0),
        "deleted_file_count": total_deleted_entries,
        "disposal_status": disposal_status,
        "body_removed": True,
        "reviewer_notes_removed": True,
        "local_packet_exported": False,
        "content_hash_of_body_stored": False,
        "body_free": True,
        "release_allowed": False,
        "p7_complete": False,
        "p8_start_allowed": False,
        "hold004_close_allowed": False,
        "purge_started_at": purge_started,
        "purge_completed_at": purge_completed,
        "p7_material_body_free": True,
    }
    assert_p7_r47_body_free_disposal_receipt_payload_contract(receipt)
    _write_json_atomic(
        layout.body_free_results_dir / "disposal_receipt.bodyfree.json",
        receipt,
        mode=0o400,
    )

    if complete:
        _project_rating_rows_after_disposal(layout)
        handoff = {
            "schema_version": HANDOFF_SCHEMA_VERSION,
            "review_session_id": layout.session_id,
            "actual_human_rating_row_count": EXPECTED_CASE_COUNT,
            "actual_r49_question_observation_row_count": EXPECTED_CASE_COUNT,
            "actual_p7_pqr_sidecar_row_count": EXPECTED_CASE_COUNT,
            "rating_rows_artifact_ref": "pass_a_rating_rows_after_disposal.bodyfree.json",
            "r49_rows_artifact_ref": "pass_b_r49_rows.bodyfree.json",
            "semantic_rows_artifact_ref": "pass_b_semantic_rows.bodyfree.json",
            "completed_sidecars_artifact_ref": "pass_c_completed_sidecars.bodyfree.json",
            "disposal_receipt_artifact_ref": "disposal_receipt.bodyfree.json",
            "pass_a_human_reviewer_ref": state.get("pass_a_human_reviewer_ref"),
            "pass_b_human_reviewer_ref": state.get("pass_b_human_reviewer_ref"),
            "pass_c_plan_overlay_resolver_ref": state.get(
                "pass_c_plan_overlay_resolver_ref"
            ),
            "actual_human_review_complete": True,
            "all_body_full_material_removed": True,
            "disposal_verified": True,
            "body_full_packet_exported": False,
            "body_derived_hash_stored": False,
            "local_absolute_path_included": False,
            "p5_decision_made_here": False,
            "p5_confirmed_candidate": False,
            "p6_start_allowed": False,
            "p8_start_allowed": False,
            "release_allowed": False,
            "body_free": True,
        }
        _assert_no_forbidden_bodyfree_keys(handoff, source="p5_actual_review_handoff")
        _write_json_atomic(
            layout.body_free_results_dir / "p5_actual_review_handoff.bodyfree.json",
            handoff,
            mode=0o400,
        )
        new_state = STATE_DISPOSAL_VERIFIED
    else:
        new_state = STATE_ABORTED_PURGED

    state = _save_state(
        layout,
        state,
        new_state=new_state,
        purged_at=purge_completed,
        body_full_material_present=False,
        disposal_verified=complete,
    )
    return _bodyfree_status(layout, state=state)


