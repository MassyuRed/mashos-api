def _print_bodyfree_json(value: Mapping[str, Any]) -> None:
    print(json.dumps(value, ensure_ascii=False, indent=2, sort_keys=True))


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="emlis_p7_p5_actual_local_review",
        description="P5 actual 24-case local-only human readfeel review controller",
    )
    parser.add_argument(
        "--local-root",
        default=None,
        help=f"external local review root。省略時は {P7_R47_LOCAL_REVIEW_ROOT_ENV_VAR}",
    )
    sub = parser.add_subparsers(dest="command", required=True)

    init = sub.add_parser("init", help="24-slot source manifest templateをexternal root内に作成")
    init.add_argument("--session-id", required=True)

    prepare = sub.add_parser("prepare", help="actual 24 source/bodyを検証しblind packetをmaterialize")
    prepare.add_argument("--session-id", required=True)
    prepare.add_argument("--reviewer-ref", required=True, help="human- で始まるpseudonymous human ref")
    prepare.add_argument("--human-reviewer-attested", action="store_true")

    pass_a = sub.add_parser("review-pass-a", help="Pass A blind readfeelを対話実行")
    pass_a.add_argument("--session-id", required=True)
    pass_a.add_argument("--reviewer-ref", required=True)
    pass_a.add_argument("--human-entry-attested", action="store_true")

    pass_b = sub.add_parser("review-pass-b", help="Pass B blind semantic observationを対話実行")
    pass_b.add_argument("--session-id", required=True)
    pass_b.add_argument("--reviewer-ref", required=True)
    pass_b.add_argument("--human-entry-attested", action="store_true")

    pass_c = sub.add_parser("review-pass-c", help="Pass C body-free plan overlayを対話実行し完了後purge")
    pass_c.add_argument("--session-id", required=True)
    pass_c.add_argument("--resolver-ref", required=True)
    pass_c.add_argument("--human-resolver-attested", action="store_true")

    status = sub.add_parser("status", help="body-free statusのみ表示")
    status.add_argument("--session-id", required=True)

    purge = sub.add_parser("purge", help="Pass C完了後のlocal-only material purge")
    purge.add_argument("--session-id", required=True)

    abort = sub.add_parser("abort", help="途中sessionをpurgeして停止")
    abort.add_argument("--session-id", required=True)
    abort.add_argument("--confirm-abort", action="store_true")
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    parser = _build_parser()
    args = parser.parse_args(argv)
    try:
        if args.command == "init":
            result = initialize_session(local_root=args.local_root, session_id=args.session_id)
        elif args.command == "prepare":
            result = prepare_session(
                local_root=args.local_root,
                session_id=args.session_id,
                reviewer_ref=args.reviewer_ref,
                human_reviewer_attested=args.human_reviewer_attested,
            )
        elif args.command == "review-pass-a":
            result = run_pass_a_interactive(
                local_root=args.local_root,
                session_id=args.session_id,
                reviewer_ref=args.reviewer_ref,
                human_entry_attested=args.human_entry_attested,
            )
        elif args.command == "review-pass-b":
            result = run_pass_b_interactive(
                local_root=args.local_root,
                session_id=args.session_id,
                reviewer_ref=args.reviewer_ref,
                human_entry_attested=args.human_entry_attested,
            )
        elif args.command == "review-pass-c":
            if not args.human_resolver_attested:
                raise ReviewOperationError("--human-resolver-attested が必要です")
            result = run_pass_c_interactive(
                local_root=args.local_root,
                session_id=args.session_id,
                resolver_ref=args.resolver_ref,
                human_resolver_attested=args.human_resolver_attested,
            )
        elif args.command == "status":
            result = session_status(local_root=args.local_root, session_id=args.session_id)
        elif args.command == "purge":
            result = purge_session(local_root=args.local_root, session_id=args.session_id)
        elif args.command == "abort":
            if not args.confirm_abort:
                raise ReviewOperationError("abortには --confirm-abort が必要です")
            result = purge_session(local_root=args.local_root, session_id=args.session_id, abort=True)
        else:  # pragma: no cover - argparse keeps this unreachable.
            parser.error("unknown command")
            return 2
    except (ReviewOperationError, ValueError) as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 2
    _print_bodyfree_json(result)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
