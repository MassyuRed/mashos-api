def _explicit_tokens() -> tuple[str, str]:
    r50 = os.environ.get(P7_R50_EXPLICIT_BODY_FULL_ALLOW_ENV_VAR, "").strip()
    r51 = os.environ.get(P7_R51_EXPLICIT_ACTUAL_LOCAL_MANUAL_RUN_ALLOW_ENV_VAR, "").strip()
    if r50 != P7_R50_EXPLICIT_BODY_FULL_ALLOW_TOKEN_REF:
        raise ReviewOperationError(f"{P7_R50_EXPLICIT_BODY_FULL_ALLOW_ENV_VAR} がexact scoped allowではありません")
    if r51 != P7_R51_EXPLICIT_ACTUAL_LOCAL_MANUAL_RUN_ALLOW_TOKEN_REF:
        raise ReviewOperationError(
            f"{P7_R51_EXPLICIT_ACTUAL_LOCAL_MANUAL_RUN_ALLOW_ENV_VAR} "
            "がexact scoped allowではありません"
        )
    return r50, r51


def prepare_session(
    *,
    local_root: str | Path | None,
    session_id: Any,
    reviewer_ref: Any,
    human_reviewer_attested: bool,
) -> dict[str, Any]:
    """Validate actual 24 source/body rows and materialize blind local packets."""

    layout = _layout(local_root, session_id)
    state = _load_state(layout)
    if state.get("state_ref") != STATE_INITIALIZED:
        raise ReviewOperationError("prepareはINITIALIZED sessionに一度だけ実行できます")
    if _retention_expired(state):
        raise ReviewOperationError("source intake期限を超えました。abortして新しいsessionを作成してください")
    if human_reviewer_attested is not True:
        raise ReviewOperationError("human reviewer attestationが必要です")
    human = _human_ref(reviewer_ref, field="reviewer_ref")
    r50_token, r51_token = _explicit_tokens()
    manifest = _validate_source_manifest(layout)
    body_full_created_at = _now()
    source_created_at = _parse_timestamp(
        manifest["created_at"],
        field="source_manifest.created_at",
    )
    intake_deadline = _parse_timestamp(
        state["intake_retention_deadline"],
        field="intake_retention_deadline",
    )
    retention_deadline = min(
        body_full_created_at
        + timedelta(hours=P7_R47_BODY_FULL_PACKET_RETENTION_HOURS),
        source_created_at
        + timedelta(hours=P7_R47_BODY_FULL_PACKET_RETENTION_HOURS),
        intake_deadline,
    )
    if retention_deadline <= body_full_created_at:
        raise ReviewOperationError(
            "actual source/body materialの72時間retention境界を超えています"
        )

    purge_plan = build_p7_r51_default_local_only_purge_plan_bodyfree(
        purge_plan_ref=f"p7_pqr_{layout.session_id}_purge_plan"
    )
    r50_decision = build_p7_r50_manual_run_decision_bodyfree(
        local_review_root_safe=True,
        explicit_allow_present=True,
        disposal_plan_ready=True,
        body_free_summary_path_ready=True,
        r49_handoff_present=True,
        reviewer_available=True,
        scope_drift_detected=False,
        body_free_leak_risk_detected=False,
        open_execution_blocker_ids=[],
        material_id=f"p7_pqr_{layout.session_id}_r50_manual_decision",
    )
    r50_preflight = build_p7_r50_local_only_root_explicit_allow_export_denylist_preflight(
        manual_run_decision_bodyfree=r50_decision,
        local_review_root=str(layout.root),
        repo_roots=[str(_AI_ROOT.parent.resolve())],
        explicit_allow_token=r50_token,
        export_candidate_refs=[],
        material_id=f"p7_pqr_{layout.session_id}_r50_preflight",
    )
    r51_preflight = build_p7_r51_local_root_explicit_allow_purge_plan_preflight(
        local_review_root=str(layout.root),
        repo_roots=[str(_AI_ROOT.parent.resolve())],
        explicit_allow_token=r51_token,
        purge_plan=purge_plan,
        export_candidate_refs=[],
        material_id=f"p7_pqr_{layout.session_id}_r51_preflight",
    )
    if r50_preflight.get("preflight_status") != "PASSED":
        raise ReviewOperationError("R50 scoped local packet preflightがPASSEDではありません")
    if r51_preflight.get("preflight_status") != "PASSED":
        raise ReviewOperationError("R51 actual local manual-run preflightがPASSEDではありません")

    controller_rows: list[dict[str, Any]] = []
    for source_row in manifest["case_rows"]:
        ordinal = int(source_row["ordinal"])
        blind = f"p5b-{secrets.token_hex(8)}"
        packet_ref = f"p5p-{secrets.token_hex(8)}"
        review_unit_ref = f"p7pqr-{layout.session_id}-u{ordinal:03d}"
        rating_ref = f"p7pqr-{layout.session_id}-rating-{ordinal:03d}"
        r49_ref = f"p7pqr-{layout.session_id}-r49-{ordinal:03d}"
        sidecar_ref = f"p7pqr-{layout.session_id}-sidecar-{ordinal:03d}"
        packet = _packet_from_source_row(
            session_id=layout.session_id,
            source_row=source_row,
            blind_case_id=blind,
            packet_ref_id=packet_ref,
        )
        _write_json_exclusive(layout.body_full_dir / _packet_filename(blind), packet)
        controller_rows.append(
            {
                "ordinal": ordinal,
                "review_unit_ref": review_unit_ref,
                "case_ref_id": source_row["case_ref_id"],
                "source_case_ref": source_row["source_case_ref"],
                "family": source_row["family"],
                "case_role": source_row["case_role"],
                "subscription_tier_ref": source_row["subscription_tier_ref"],
                "expected_boundary_audit_ref": source_row["expected_boundary_audit_ref"],
                "source_snapshot_ref": source_row["source_snapshot_ref"],
                "source_runtime_ref": source_row["source_runtime_ref"],
                "currentness_status_ref": source_row["currentness_status_ref"],
                "body_source_kind_ref": source_row["body_source_kind_ref"],
                "blind_case_id": blind,
                "packet_ref_id": packet_ref,
                "linked_human_rating_row_ref": rating_ref,
                "source_question_observation_ref": r49_ref,
                "sidecar_row_ref": sidecar_ref,
                "controller_only": True,
                "blind_case_id_derived_from_body_or_record_hash": False,
                "body_free": True,
            }
        )

    controller = {
        "schema_version": CONTROLLER_MANIFEST_SCHEMA_VERSION,
        "review_session_id": layout.session_id,
        "snapshot_ref": manifest["snapshot_ref"],
        "runtime_revision_ref": manifest["runtime_revision_ref"],
        "source_material_ref": manifest["source_material_ref"],
        "supplied_user_execution_instruction_ref": manifest[
            "supplied_user_execution_instruction_ref"
        ],
        "currentness_status_ref": CURRENTNESS_READY_STATUS,
        "pass_a_human_reviewer_ref": human,
        "pass_a_human_reviewer_attested": True,
        "case_count": len(controller_rows),
        "case_rows": controller_rows,
        "pass_order_refs": ["PASS_A_BLIND_READFEEL", "PASS_B_BLIND_SEMANTIC", "PASS_C_BODYFREE_PLAN_OVERLAY"],
        "family_and_tier_hidden_in_pass_a": True,
        "family_and_tier_hidden_in_pass_b": True,
        "body_full_surface_visible_in_pass_c": False,
        "backward_pass_mutation_allowed": False,
        "body_full_packet_export_allowed": False,
        "body_derived_hash_stored": False,
        "local_absolute_path_included": False,
        "p5_decision_made_here": False,
        "p6_start_allowed": False,
        "p8_start_allowed": False,
        "release_allowed": False,
        "body_free": True,
    }
    _assert_no_forbidden_bodyfree_keys(controller, source="controller_manifest")
    _write_json_exclusive(layout.controller_manifest_file, controller, mode=0o400)

    audit = {
        "schema_version": PREFLIGHT_AUDIT_SCHEMA_VERSION,
        "review_session_id": layout.session_id,
        "r47_local_root_status_ref": "VALID_EXTERNAL_LOCAL_ONLY",
        "r50_manual_run_decision_ref": r50_decision.get("manual_run_decision"),
        "r50_preflight_status_ref": r50_preflight.get("preflight_status"),
        "r51_preflight_status_ref": r51_preflight.get("preflight_status"),
        "r50_scoped_allow_present": True,
        "r51_scoped_allow_present": True,
        "scoped_allow_value_stored": False,
        "explicit_current_source_case_count": EXPECTED_CASE_COUNT,
        "actual_body_full_packet_count": EXPECTED_CASE_COUNT,
        "supplied_user_execution_instruction_ref": manifest[
            "supplied_user_execution_instruction_ref"
        ],
        "pass_a_human_reviewer_ref": human,
        "pass_a_human_reviewer_attested": True,
        "reviewer_free_text_persisted": False,
        "reviewer_free_text_immediate_disposal": True,
        "body_full_packet_export_allowed": False,
        "body_derived_hash_stored": False,
        "local_absolute_path_included": False,
        "body_free": True,
    }
    _assert_no_forbidden_bodyfree_keys(audit, source="preflight_audit")
    _write_json_exclusive(layout.audit_dir / "preflight.bodyfree.json", audit, mode=0o400)

    # Once packets exist, both source and packet bodies are immutable until purge.
    _freeze_tree(layout.body_full_dir)
    state = _save_state(
        layout,
        state,
        new_state=STATE_PASS_A_OPEN,
        source_body_material_created_at=_iso(source_created_at),
        body_full_created_at=_iso(body_full_created_at),
        body_full_retention_deadline=_iso(retention_deadline),
        body_full_packet_count=EXPECTED_CASE_COUNT,
        pass_a_human_reviewer_ref=human,
        pass_a_human_reviewer_attested=True,
        body_full_material_present=True,
    )
    return _bodyfree_status(layout, state=state)


