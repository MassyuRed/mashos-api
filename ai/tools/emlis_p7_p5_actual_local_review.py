def _assert_completed_sidecar(
    row: Mapping[str, Any],
    *,
    controller_row: Mapping[str, Any],
    rating_row: Mapping[str, Any],
    r49_row: Mapping[str, Any],
    semantic_row: Mapping[str, Any],
) -> None:
    data = dict(row)
    if set(data) != COMPLETED_SIDECAR_FIELDS:
        raise ReviewOperationError("completed sidecar fieldが§14.4 exact schemaと一致しません")
    _assert_no_forbidden_bodyfree_keys(data, source="completed_sidecar")
    fixed = {
        "schema_version": COMPLETED_SIDECAR_SCHEMA_VERSION,
        "observation_source_ref": "HUMAN_REVIEWER",
        "review_lane_ref": "P5_HUMAN_BLIND_QA",
        "source_contract_mode_ref": "R49_EXACT_REFERENCE",
        "plan_overlay_source_ref": "SAFE_METADATA_POST_BLIND_FREEZE",
        "blind_rating_frozen_before_plan_overlay": True,
        "semantic_observation_frozen_before_plan_overlay": True,
        "question_text_included": False,
        "draft_question_text_included": False,
        "raw_answer_included": False,
        "reviewer_free_text_included": False,
        "body_removed": True,
        "body_free": True,
    }
    for key, expected in fixed.items():
        if data.get(key) != expected:
            raise ReviewOperationError(f"completed sidecar {key} fixed valueが一致しません")
    if (
        data.get("review_session_id") != rating_row.get("review_session_id")
        or data.get("review_session_id") != r49_row.get("review_session_id")
    ):
        raise ReviewOperationError("sidecar session identityがR48/R49 rowと一致しません")
    identity_keys = ("packet_ref_id", "blind_case_id", "case_ref_id")
    if any(rating_row.get(key) != r49_row.get(key) for key in identity_keys):
        raise ReviewOperationError("R48/R49 identity quartetが一致しません")
    if data.get("review_unit_ref") != controller_row.get("review_unit_ref"):
        raise ReviewOperationError("sidecar review_unit_refがcontrollerと一致しません")
    if data.get("linked_human_rating_row_ref") != controller_row.get("linked_human_rating_row_ref"):
        raise ReviewOperationError("sidecar rating row refがcontrollerと一致しません")
    if data.get("source_question_observation_ref") != controller_row.get("source_question_observation_ref"):
        raise ReviewOperationError("sidecar R49 row refがcontrollerと一致しません")
    if data.get("family_ref") != controller_row.get("family"):
        raise ReviewOperationError("sidecar family_refがcontrollerと一致しません")
    if data.get("source_primary_class_ref") != r49_row.get("question_need_primary_class"):
        raise ReviewOperationError("sidecar source primaryがR49 rowと一致しません")
    if list(data.get("ambiguity_kind_refs") or []) != list(r49_row.get("ambiguity_kind_refs") or []):
        raise ReviewOperationError("sidecar ambiguity refsはR49 exact rowと一致する必要があります")
    for key in (
        "human_reviewer_ref",
        "reviewed_at",
        "linked_human_rating_row_ref",
        "source_question_observation_ref",
        "source_primary_class_ref",
        "reason_family_ref",
        "one_question_fit_ref",
        "preliminary_observation_possible_ref",
        "question_kind_candidate_ref",
        "answer_affordance_candidate_refs",
        "interrogation_risk_ref",
        "self_blame_amplification_risk_ref",
        "immediate_observation_delay_risk_ref",
        "repair_target_refs",
        "question_system_eligibility_ref",
        "refined_observation_eligibility_ref",
        "legacy_plan_class_primary_present",
        "manual_reason_family_mapping_status_ref",
        "sanitized_reason_ids",
    ):
        if data.get(key) != semantic_row.get(key):
            raise ReviewOperationError(f"Pass Cはfreeze済みPass B field {key}を変更できません")
    _human_ref(data.get("human_reviewer_ref"), field="human_reviewer_ref")
    _human_ref(data.get("plan_overlay_resolver_ref"), field="plan_overlay_resolver_ref")
    if data.get("plan_candidate_ref") not in PLAN_CANDIDATE_REFS:
        raise ReviewOperationError("plan candidate refが不正です")
    rating_time = _parse_timestamp(rating_row.get("reviewed_at"), field="rating.reviewed_at")
    semantic_time = _parse_timestamp(data.get("reviewed_at"), field="sidecar.reviewed_at")
    overlay_time = _parse_timestamp(data.get("plan_overlay_resolved_at"), field="sidecar.plan_overlay_resolved_at")
    if not rating_time <= semantic_time <= overlay_time:
        raise ReviewOperationError("Pass A <= Pass B <= Pass C timestamp順が成立しません")
    if (
        data.get("legacy_plan_class_primary_present") is not False
        or data.get("manual_reason_family_mapping_status_ref") != "NOT_REQUIRED"
    ):
        raise ReviewOperationError("新規Pass B/Cではlegacy plan primaryを使用できません")
    reason_family = data.get("reason_family_ref")
    if reason_family not in REASON_FAMILY_REFS:
        raise ReviewOperationError("sidecar reason familyが不正です")
    plan_candidate = data.get("plan_candidate_ref")
    if reason_family == "INSUFFICIENT_MATERIAL" and plan_candidate != "UNRESOLVED":
        raise ReviewOperationError("insufficient materialのplan overlayはUNRESOLVED固定です")
    if reason_family in {
        "EMLIS_OBSERVABLE_WITHOUT_QUESTION",
        "QUESTION_WOULD_BURDEN_IMMEDIATE_OBSERVATION",
        "NOT_QUESTION_REPAIR_OR_BOUNDARY",
    } and plan_candidate != "NONE":
        raise ReviewOperationError("question candidateでないreason familyのplan overlayはNONE固定です")


